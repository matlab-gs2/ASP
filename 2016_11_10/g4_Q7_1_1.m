clear all; close all; clc;

fs = 8000;
tt = (1:fs)/fs;
f = 500;
xx1 = sin(2*pi*f*tt);
xx2 = 0.5*sin(2*pi*1000*tt);
xx3 = xx1 + xx2;
xx1 = xx1';
xx2 = xx2';
xx3 = xx3';
figure(1);
subplot(3,1,1);
plot(xx1);
axis([0 100 -1 1]);
subplot(3,1,2);
plot(xx2);
axis([0 100 -1 1]);
subplot(3,1,3);
plot(xx3);
axis([0 100 -2 2]);

figure(2);
N = 2^10;
xx1_0 = xx1(1:N);
xx1_1 = xx1_0 .* hanning(N);
xx1_2 = [xx1_1; zeros(N, 1)];
X = fft(xx1_2);
Xamp = abs(X(1:N+1));
ff = (0:N) * (4000/N);
plot(ff, Xamp);
xlabel('é¸îgêî(Hz)');
hold on;
xx2_0 = xx2(1:N);
xx2_1 = xx2_0 .* hanning(N);
xx2_2 = [xx2_1; zeros(N, 1)];
X = fft(xx2_2);
Xamp = abs(X(1:N+1));
plot(ff, Xamp, 'r');
hold on;
xx3_0 = xx3(1:N);
xx3_1 = xx3_0 .* hanning(N);
xx3_2 = [xx3_1; zeros(N, 1)];
X = fft(xx3_2);
Xamp = abs(X(1:N+1));
figure(3);
plot(ff, Xamp);

return;